// The Text widget in Flutter is used to display a short piece of text on the screen.
// The text can be styled using various properties, such as font size, font weight, color, and more.

// Here's an example of how to use the Text widget in Flutter with some styling properties:

Text(
  "Hello Text",
  style: TextStyle(
    fontSize: 25,
    fontWeight: FontWeight.bold,
    color: Colors.blue[900],
  ),
)


// In this example, the Text widget takes two arguments: the text to be 
// displayed and a TextStyle object that sets the style for the text.
// The TextStyle object has the following properties
// fontSize: sets the font size of the text in logical pixels.
// fontWeight: sets the font weight of the text, using the FontWeight enum.
// color: sets the color of the text, using the Colors class.
// There are many other properties available in the TextStyle object that you can use to further customize the look and feel of your text.
